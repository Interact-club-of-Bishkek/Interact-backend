#!/bin/bash

# Устанавливает команду Entrypoint для Django-контейнера.
# Ждет, пока БД будет доступна, затем применяет миграции и запускает Gunicorn.

# ----------------------------------------------------
# 1. ОЖИДАНИЕ БАЗЫ ДАННЫХ (имя сервиса 'db' и порт 5432)
# ----------------------------------------------------
echo "Ожидание PostgreSQL..."

# Используем 'nc -z' (netcat) для проверки доступности порта
while ! nc -z db 5432; do
  sleep 0.5
done

echo "PostgreSQL запущен!"

# ----------------------------------------------------
# 2. УСТАНОВКА ПРАВ ДОСТУПА К ТОМАМ (КРИТИЧЕСКОЕ ИСПРАВЛЕНИЕ)
# ----------------------------------------------------
# Выполняем chmod здесь, чтобы гарантировать, что права применены
# к смонтированным томам, а не к пустым папкам в образе.
echo "Установка прав доступа (775) для томов media и staticfiles..."
chmod -R 775 /app/media
chmod -R 775 /app/staticfiles

# ----------------------------------------------------
# 3. ПОДГОТОВКА (Миграции и Статика)
# ----------------------------------------------------
echo "Применение миграций Django..."
# gosu не требуется, так как эти команды запускаются как root (или пользователь entrypoint),
# но выполняют действия, которые нужны перед переключением пользователя.
python manage.py migrate --noinput

echo "Сбор статических файлов..."
python manage.py collectstatic --noinput

# ----------------------------------------------------
# 4. ЗАПУСК GUNICORN
# ----------------------------------------------------
echo "Запуск Gunicorn..."
# Используем gosu для безопасного запуска Gunicorn от имени не-root пользователя 'appuser'
exec gosu appuser gunicorn interact.wsgi:application --bind 0.0.0.0:8000 --workers 3